test.describe("AC-D2 — 11 MB file upload shows size-limit error", () => {
  test("uploading an 11 MB file shows the size-limit message (10 MB cap)", async ({ page }) => {
    await gotoScan(page);

    // Create an 11 MB buffer — content doesn't matter; the size check happens
    // before any parsing (dispatch.ts: `if (file.size > limit) return too-large`).
    const elevenMbBuffer = Buffer.alloc(11 * 1024 * 1024, 0x00);
    await uploadFile(page, {
      name: "huge-document.pdf",
      mimeType: "application/pdf",
      buffer: elevenMbBuffer,
    });

    // The error message includes the file size and the 10 MB cap
    await expect(
      page.locator("text=/free plan caps file scans at 10 MB/i"),
    ).toBeVisible({ timeout: 10_000 });
  });

  test("size-limit error shows the actual file size", async ({ page }) => {
    await gotoScan(page);

    const elevenMbBuffer = Buffer.alloc(11 * 1024 * 1024, 0x00);
    await uploadFile(page, {
      name: "huge-document.pdf",
      mimeType: "application/pdf",
      buffer: elevenMbBuffer,
    });

    // describeParseError formats sizeBytes: `${(e.sizeBytes / 1024 / 1024).toFixed(1)} MB`
    // 11 MB → "11.0 MB"
    await expect(
      page.locator("text=/11\\.0 MB/"),
    ).toBeVisible({ timeout: 10_000 });
  });

  test("no scan runs when file is over the size limit", async ({ page }) => {
    await gotoScan(page);

    const elevenMbBuffer = Buffer.alloc(11 * 1024 * 1024, 0x00);
    await uploadFile(page, {
      name: "huge-document.pdf",
      mimeType: "application/pdf",
      buffer: elevenMbBuffer,
    });

    // Wait for the error to appear
    await page.locator("text=/free plan caps file scans at 10 MB/i").waitFor({ timeout: 10_000 });

    // The "scanning…" indicator must never have appeared (no scan was started)
    const scanningIndicator = page.locator("text=Scanning");
    await expect(scanningIndicator).not.toBeVisible();

    // No findings section should be present (no scan completed)
    await expect(page.locator('[aria-label="Scan findings"]')).not.toBeVisible();
  });
});


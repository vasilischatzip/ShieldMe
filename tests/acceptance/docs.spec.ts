import { test, expect } from "@playwright/test";
import { join, dirname } from "path";
import { fileURLToPath } from "url";
import { readFileSync } from "fs";

const __filename  = fileURLToPath(import.meta.url);
const __dirname   = dirname(__filename);
const SAMPLES_DIR = join(__dirname, "..", "fixtures", "samples");

/* ── Helpers ──────────────────────────────────────────────────── */

/** Navigate to /scan and wait for the page shell. */
async function gotoScan(page: import("@playwright/test").Page) {
  await page.goto("/scan");
  await page.waitForSelector('[aria-label="Primary navigation"]', { timeout: 10_000 });
}

/** Upload a file via the hidden file input.
 *  Playwright's setInputFiles works even on `display:none` inputs. */
async function uploadFile(
  page: import("@playwright/test").Page,
  fileData: { name: string; mimeType: string; buffer: Buffer },
) {
  const fileInput = page.locator('input[type="file"]');
  await fileInput.setInputFiles(fileData);
}

/** Wait for the scan to complete and return the visible results section. */
async function waitForScanDone(page: import("@playwright/test").Page) {
  // The scan complete state shows either "Found N items" or "Nothing sensitive found"
  await page.waitForSelector(
    '[aria-label="Scan findings"], [role="status"]:has-text("Nothing")',
    { timeout: 30_000 },
  );
}


test.describe("AC-D1 — scan tax-return PDF reports planted PII", () => {
  test("scanning tax-return-2025.pdf finds the SSN", async ({ page }) => {
    await gotoScan(page);

    // Upload the fixture PDF (generated by global-setup.ts)
    const pdfBytes = readFileSync(join(SAMPLES_DIR, "tax-return-2025.pdf"));
    await uploadFile(page, {
      name: "tax-return-2025.pdf",
      mimeType: "application/pdf",
      buffer: Buffer.from(pdfBytes),
    });

    await waitForScanDone(page);

    // The SSN detector belongs to myIdentity (ON by default).
    // FindingsList groups by category — the "My Identity" card should appear.
    const identityGroup = page.locator('[aria-label="Scan findings"]')
      .locator("text=My Identity");
    await expect(identityGroup).toBeVisible({ timeout: 5_000 });
  });

  test("scanning tax-return-2025.pdf finds the IBAN", async ({ page }) => {
    await gotoScan(page);

    const pdfBytes = readFileSync(join(SAMPLES_DIR, "tax-return-2025.pdf"));
    await uploadFile(page, {
      name: "tax-return-2025.pdf",
      mimeType: "application/pdf",
      buffer: Buffer.from(pdfBytes),
    });

    await waitForScanDone(page);

    // IBAN detector belongs to myMoney (ON by default).
    const moneyGroup = page.locator('[aria-label="Scan findings"]')
      .locator("text=My Money");
    await expect(moneyGroup).toBeVisible({ timeout: 5_000 });
  });

  test("SSN finding contextSnippet references page 1", async ({ page }) => {
    await gotoScan(page);

    const pdfBytes = readFileSync(join(SAMPLES_DIR, "tax-return-2025.pdf"));
    await uploadFile(page, {
      name: "tax-return-2025.pdf",
      mimeType: "application/pdf",
      buffer: Buffer.from(pdfBytes),
    });

    await waitForScanDone(page);

    // The PDF parser prepends "[Page 1]" to page text.
    // The contextSnippet shown in the UI should include this label.
    const findingsSection = page.locator('[aria-label="Scan findings"]');
    await expect(findingsSection).toBeVisible();
    const sectionText = await findingsSection.textContent() ?? "";

    // SSN was planted on page 1 — context should reference it
    expect(sectionText).toContain("Page 1");
  });
});

